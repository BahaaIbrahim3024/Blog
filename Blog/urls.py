from django.contrib import admin
from django.urls import path, include, re_path
from home.views import home_screen_view
from django.contrib.auth import views as auth_views
from django.conf import settings
from django.conf.urls.static import static
from account.views import must_authenticate_view
from rest_framework.urlpatterns import format_suffix_patterns


urlpatterns = [
    # Basic urls
    path('', home_screen_view, name="home"),
    path('admin/', admin.site.urls),
    path('account/', include('account.urls')),
    path('blog/', include('blogPost.urls', namespace='blogPost')),
    path('must_authenticate/', must_authenticate_view, name='must_authenticate'),
    path('home/',include('home.urls'), name='api'),

    # APIs URLs
    path('api/blog/', include('blogPost.api.urls', namespace='blog_api')),
    path('api/account/', include('account.api.urls', namespace='account_api')),


    # Password reset links
    path('password_change/done/',
         auth_views.PasswordChangeDoneView.as_view(template_name='registration/password_change_done.html'),
         name='password_change_done'),

    path('password_change/', auth_views.PasswordChangeView.as_view(template_name='registration/password_change.html'),
         name='password_change'),

    path('password_reset/done/',
         auth_views.PasswordResetCompleteView.as_view(template_name='registration/password_reset_done.html'),
         name='password_reset_done'),

    path('reset/<uidb64>/<token>/', auth_views.PasswordResetConfirmView.as_view(), name='password_reset_confirm'),
    path('password_reset/', auth_views.PasswordResetView.as_view(), name='password_reset'),

    path('reset/done/',
         auth_views.PasswordResetCompleteView.as_view(template_name='registration/password_reset_complete.html'),
         name='password_reset_complete'),
]

urlpatterns = format_suffix_patterns(urlpatterns)

if settings.DEBUG:
    urlpatterns += static(settings.STATIC_URL, document_root=settings.STATIC_ROOT)
    urlpatterns += static(settings.MEDIA_URL, document_root=settings.MEDIA_ROOT)